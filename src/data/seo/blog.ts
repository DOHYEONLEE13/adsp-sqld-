export function findPostBySlug(slug: string): BlogPost | undefined {
  // URL-encoded 한국어 슬러그도 매칭
  const decoded = decodeURIComponent(slug);
  return ALL_BLOG_POSTS.find(
    (p) => p.slug === slug || p.slug === decoded,
  );
}

