/**
 * blog.ts — Tier 2 SEO 코너스톤 블로그 포스트.
 *
 * 라우트: `/blog` (인덱스) + `/blog/:slug` (단일 포스트)
 *
 * 콘텐츠 원칙:
 *   - 사실 정확성 1순위. 시험 일정·합격 기준은 KDATA 공식 정보 기반.
 *   - 한국어 자연스럽게. 직역체 금지.
 *   - 비교/순서/로드맵 같은 high-intent 키워드 직접 타깃.
 *   - 본문 1,500~2,500자, 읽는 시간 5~10분.
 *   - FAQ 섹션 동봉 (FAQ JSON-LD → PAA 진입).
 *   - 내부 링크: 같은 토픽의 lesson · curriculum · faq · glossary 로 연결.
 *
 * UGC(후기) 조작 금지 — 사용자가 실제로 쓴 후기가 아니면 "후기" 톤 회피.
 * 대신 "가이드 / 로드맵 / 비교" 같은 정직한 framing 으로 대체.
 */

export type BlogBlock =
  | { kind: 'p'; text: string }
  | { kind: 'h2'; text: string; id?: string }
  | { kind: 'h3'; text: string; id?: string }
  | { kind: 'ul'; items: string[] }
  | { kind: 'ol'; items: string[] }
  | {
      kind: 'callout';
      tone: 'tip' | 'warn' | 'note';
      title?: string;
      body: string;
    }
  | { kind: 'table'; headers: string[]; rows: string[][] }
  | { kind: 'quote'; text: string; cite?: string }
  | { kind: 'cta'; label: string; href: string };

export interface BlogPost {
  slug: string;
  /** 페이지 H1 + meta title 베이스. */
  title: string;
  /** 부제 (H1 아래 짧은 설명). */
  subtitle: string;
  /** 카테고리. blog 인덱스 그룹핑 + breadcrumb. */
  category: 'comparison' | 'roadmap' | 'guide';
  /** 키워드 — 페이지 본문에 자연 등장. */
  primaryKeyword: string;
  publishedAt: string;
  updatedAt?: string;
  readingMinutes: number;
  /** OG · meta description. 160자 이내. */
  metaDescription: string;
  blocks: BlogBlock[];
  /** FAQ 섹션 — 자동으로 본문 끝에 렌더 + JSON-LD FAQPage 동봉. */
  faqs?: Array<{ q: string; a: string }>;
  /** 같은 카테고리·토픽 cross-link. */
  relatedSlugs?: string[];
}

